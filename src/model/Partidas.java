package model;

/**
 *
 * @author Taynara Garcia Teixeira
 */
public class Partidas {
    protected String jogador;
    protected String campeao;
    protected String posicao;
    protected String funcao_part;
    protected String tipo;
    protected int id;


    /**
     * @return the jogador
     */
    public String getJogador() {
        return jogador;
    }

    /**
     * @param jogador the jogador to set
     */
    public void setJogador(String jogador) {
        this.jogador = jogador;
    }

    /**
     * @return the campeao
     */
    public String getCampeao() {
        return campeao;
    }

    /**
     * @param campeao the campeao to set
     */
    public void setCampeao(String campeao) {
        this.campeao = campeao;
    }

    /**
     * @return the posicao
     */
    public String getPosicao() {
        return posicao;
    }

    /**
     * @param posicao the posicao to set
     */
    public void setPosicao(String posicao) {
        this.posicao = posicao;
    }

    /**
     * @return the funcao_part
     */
    public String getFuncao_part() {
        return funcao_part;
    }

    /**
     * @param funcao_part the funcao_part to set
     */
    public void setFuncao_part(String funcao_part) {
        this.funcao_part = funcao_part;
    }

    /**
     * @return the tipo
     */
    public String getTipo() {
        return tipo;
    }

    /**
     * @param tipo the tipo to set
     */
    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    
    
    
    
}
